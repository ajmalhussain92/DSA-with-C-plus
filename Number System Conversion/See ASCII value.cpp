#include <iostream>
using namespace std;

int main () {
    
    cout << int('a') << endl;		// 97
    cout << int('z') << endl;		// 122
    cout << int('A') << endl;		// 65
    cout << int('Z') << endl;		// 90
    cout << int('0') << endl;		// 48
    cout << int('*') << endl;		// 42

    return 0;
}